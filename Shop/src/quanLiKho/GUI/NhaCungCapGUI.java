    private void jb_themActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jb_themActionPerformed
        tf_ma.setText("");
        String ma = tf_ma.getText();
        String ten = tf_ten.getText();
        String diachi = tf_diachi.getText();
        int sdt = Integer.parseInt(tf_sdt.getText());
        String email = tf_email.getText();
        
        if (ma!= null || ten == null || diachi==null || email==null) {
            JOptionPane.showMessageDialog(null, "Vui lòng nhập đủ thông tin!");
        } else {
            ++ soNCC;        
            if (soNCC<10) {
                ma = "0" + soNCC;
            } else  ma = String.valueOf(soNCC);
            NhaCungCapDTO ncc = new NhaCungCapDTO(ma, ten, diachi, email, sdt, 1);
            int kq = nccBLL.updateSelected(ncc);
            if (kq != 0) {
                JOptionPane.showMessageDialog(null, "Thêm thành công!");
                docDuLieuNCC();
                jb_lammoiActionPerformed(evt);
            } else JOptionPane.showMessageDialog(null, "Thêm thất bại!");  
        }
    }//GEN-LAST:event_jb_themActionPerformed


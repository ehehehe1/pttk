    private void jb_xoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jb_xoaActionPerformed
        int row = (int) table.getSelectedRow();
        if (row != -1) {
            String ma = (String) table.getValueAt(row, 0);
            int kq = nccBLL.deleteSelected(new NhaCungCapDTO(ma));
            if (kq != 0) {
                JOptionPane.showMessageDialog(null, "Xoá thành công!");
                docDuLieuNCC();
            } else JOptionPane.showMessageDialog(null, "Xoá thất bại!");  
        } else JOptionPane.showMessageDialog(null, "Vui lòng chọn dòng cần xoá!");
    }//GEN-LAST:event_jb_xoaActionPerformed


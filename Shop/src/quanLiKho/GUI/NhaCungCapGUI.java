    private void docDuLieuNCC() {
        ArrayList<NhaCungCapDTO> list = nccBLL.getSelectedAll();
        if (!list.isEmpty()) {
            for (NhaCungCapDTO t : list) {
                Object[] data = new Object[]{t.getMaNCC(), t.getTenNCC(), t.getSdt(), t.getEmail(), t.getDiaChi()};
                model.addRow(data);
                model.fireTableDataChanged();
            }
        }
    }
    

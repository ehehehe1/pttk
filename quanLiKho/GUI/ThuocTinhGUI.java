    private void docDuLieuLoaiSP() {
        ArrayList<LoaiSPDTO> list = loaiBLL.getSelectedAll();
        if (!list.isEmpty()) {
            for (LoaiSPDTO t : list) {
                Object[] data = new Object[]{t.getMaLoai(), t.getTenLoai()};
                model_loai.addRow(data);
                model_loai.fireTableDataChanged();
            }
        }
    }
    
    private void docDuLieuMau() {
        ArrayList<MauSacDTO> list = mauBLL.getSelectedAll();
        if (!list.isEmpty()) {
            for (MauSacDTO t : list) {
                Object[] data = new Object[]{t.getMaMau(), t.getTenMau()};
                model_mau.addRow(data);
                model_mau.fireTableDataChanged();
            }
        }
    }
    
    private void docDuLieuSize() {
        ArrayList<SizeDTO> list = sizeBLL.getSelectedAll();
        if (!list.isEmpty()) {
            for (SizeDTO t : list) {
                Object[] data = new Object[]{t.getMaSize(), t.getTenSize()};
                model_size.addRow(data);
                model_size.fireTableDataChanged();
            }
        }
    }
    
    

    private void jb_themActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jb_themActionPerformed
        String loaitk = (String) cb_timkiem.getSelectedItem();
        String text = (String) tf_timkiem.getText();
        int kq = 0;
        if (loaitk.equals("Loại sản phẩm")) {
            ++ soLoai;
            String maLoai = null;
            if (soLoai<10) {
                maLoai = "0" + soLoai;
            } else  maLoai = String.valueOf(soLoai);
            kq = loaiBLL.insertSelected(new LoaiSPDTO(maLoai, text, 1));
            
        } else if (loaitk.equals("Màu sắc")) {
            ++ soMau;
            String maMau = null;
            if (soMau<10) {
                maMau = "0" + soMau;
            } else  maMau = String.valueOf(soMau);
            kq = mauBLL.insertSelected(new MauSacDTO(maMau, text, 1));
            
        } else if (loaitk.equals("Kích thước")) {
            ++ soSize;
            String maSize = null;
            if (soSize<10) {
                maSize = "0" + soLoai;
            } else  maSize = String.valueOf(soLoai);
            kq = sizeBLL.insertSelected(new SizeDTO(maSize, text, 1));
        }
        if (kq != 0) {
            JOptionPane.showMessageDialog(null, "Thêm thành công!");
            jb_xemtatcaActionPerformed(evt);
        } else JOptionPane.showMessageDialog(null, "Thêm thất bại!");  
    }//GEN-LAST:event_jb_themActionPerformed

    private void jb_xoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jb_xoaActionPerformed
        String loaitk = (String) cb_timkiem.getSelectedItem();
        int kq = 0;
        if (loaitk.equals("Loại sản phẩm")) {
            int row = (int) tb_loai.getSelectedRow();
            if (row != -1) {
               String ma = (String) tb_loai.getValueAt(row, 0);
               kq = loaiBLL.deleteSelected(new LoaiSPDTO(ma));
            } else {
                JOptionPane.showMessageDialog(null, "Vui lòng chọn loại sản phẩm cần xoá!");
            }
        } else if (loaitk.equals("Màu sắc")) {
            int row = (int) tb_mau.getSelectedRow();
            if (row != -1) {
               String ma = (String) tb_mau.getValueAt(row, 0);
               kq = mauBLL.deleteSelected(new MauSacDTO(ma));
            } else {
                JOptionPane.showMessageDialog(null, "Vui lòng chọn màu cần xoá!");
            }
        } else if (loaitk.equals("Kích thước")) {
            int row = (int) tb_size.getSelectedRow();
            if (row != -1) {
               String ma = (String) tb_size.getValueAt(row, 0);
               kq = sizeBLL.deleteSelected(new SizeDTO(ma));
            } else {
                JOptionPane.showMessageDialog(null, "Vui lòng chọn kích thước cần xoá!");
            }
        }
        if (kq != 0) {
            JOptionPane.showMessageDialog(null, "Xoá thành công!");
            jb_xemtatcaActionPerformed(evt);
        } else JOptionPane.showMessageDialog(null, "Xoá thất bại!");  
    }//GEN-LAST:event_jb_xoaActionPerformed

